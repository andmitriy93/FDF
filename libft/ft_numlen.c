#include "libft.h"

int		ft_numlen(long n, int base)
{
	int	len;

	if (n == 0)
		return (1);
	if (n < 0)
		len = 1;
	else
		len = 0;
	while (n != 0)
	{
		n /= base;
		len++;
	}
	return (len);
}
